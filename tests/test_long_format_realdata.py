def test_internal_fragment_map_matches_current_original():
    """Our InternalFragmentMap port matches the CURRENT FLASHApp algorithm.

    (The bundled internal_fragment_data.pkl.gz is stale — generated by an older
    algorithm version while the feature was disabled — so we compare against the
    live src.render.sequence implementation, not the cached file.)
    """
    from openms_insight.components.internalfragmentmap import (
        internal_fragment_masses,
    )
    from src.render.sequence import getInternalFragmentDataFromSeq

    tnt_dirs = glob.glob(
        os.path.join(
            DEFAULT_WS, "flashtnt", "cache", "files", "*antibody*"
        )
    )
    if not tnt_dirs:
        pytest.skip("TnT antibody example not available")
    with gzip.open(os.path.join(tnt_dirs[0], "sequence_data.pkl.gz"), "rb") as f:
        seqdata = pickle.load(f)

    seq = "".join(seqdata[0]["sequence"])
    ref = getInternalFragmentDataFromSeq(seq)
    for ion_type in ("by", "bz", "cy"):
        ours = internal_fragment_masses(seq, ion_type)[0]
        assert len(ours) == len(ref[f"fragment_masses_{ion_type}"])


def test_featureview_explode_real_quant():
    from openms_insight import FeatureView

    q = os.path.join(
        DEFAULT_WS, "flashquant", "cache", "files", "example", "quant_dfs.pq"
    )
    if not os.path.isfile(q):
        pytest.skip("quant example not available")
    qdf = pl.read_parquet(q)
    long = FeatureView.explode_traces(qdf)
    assert long.height > 0
    assert set(long.columns) >= {
        "feature_group",
        "charge",
        "mz",
        "rt",
        "intensity",
    }
    # Every feature group present in the source appears in the long output
    assert set(long["feature_group"].unique().to_list()) <= set(
        qdf["FeatureGroupIndex"].to_list()
    )
